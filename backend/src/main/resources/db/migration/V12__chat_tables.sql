CREATE TYPE public.acknowledgement_status_enum AS ENUM ('PENDING', 'ACKNOWLEDGED');
CREATE TYPE public.coordination_status_enum AS ENUM ('PENDING', 'APPROVED', 'REJECTED');
CREATE TYPE public.message_type_enum AS ENUM (
    'TEXT', 
    'ATTACHMENT', 
    'MENTION', 
    'COORDINATION_REQUEST', 
    'ACKNOWLEDGEMENT_REQUEST', 
    'COORDINATION_RESPONSE', 
    'ACKNOWLEDGEMENT_RESPONSE', 
    'SYSTEM_ACTION'
);


CREATE TABLE IF NOT EXISTS public.chat_messages (
    id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    acknowledgement_status acknowledgement_status_enum,
    content text NOT NULL,
    coordination_status coordination_status_enum,
    message_type message_type_enum,
    meta_data text,
    "timestamp" timestamp(6) without time zone NOT NULL,
    attached_file_id bigint UNIQUE,
    conversation_id bigint NOT NULL,
    related_message_id bigint,
    sender_id bigint NOT NULL,
    FOREIGN KEY (conversation_id) REFERENCES public.conversations(id) ON DELETE CASCADE,
    FOREIGN KEY (related_message_id) REFERENCES public.chat_messages(id),
    FOREIGN KEY (sender_id) REFERENCES public.users(id),
    FOREIGN KEY (attached_file_id) REFERENCES public.files(id)
);


